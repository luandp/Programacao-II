public class Cachorro extends Mamifero{
    private String string1000;
    private String string2000;
    private String String3000;
    private int a2000;
    private double double3000;

    public Cachorro() {
        this.string1000 = "";
        this.string2000 = "";
        this.String3000 = "";
        this.a2000 = 0;
        this.double3000 = 0;
    }

    
    
    public String getString1000() {
        return string1000;
    }

    public void setString1000(String string1000) {
        this.string1000 = string1000;
    }

    public String getString2000() {
        return string2000;
    }

    public void setString2000(String string2000) {
        this.string2000 = string2000;
    }

    public String getString3000() {
        return String3000;
    }

    public void setString3000(String String3000) {
        this.String3000 = String3000;
    }

    public int getA2000() {
        return a2000;
    }

    public void setA2000(int a2000) {
        this.a2000 = a2000;
    }

    public double getDouble3000() {
        return double3000;
    }

    public void setDouble3000(double double3000) {
        this.double3000 = double3000;
    }
    
    
}
